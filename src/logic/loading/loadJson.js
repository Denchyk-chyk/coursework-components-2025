/**
 * Асинхронна функція для завантаження JSON-файлу за заданим шляхом і парсингу його в об’єкт.
 *
 * @param {string} path - Шлях або URL до JSON-файлу.
 * @returns {Promise<any>} - Обіцянка, що розв’язується отриманим із JSON об’єктом або масивом.
 *
 * Функція виконує HTTP-запит на вказаний шлях, перевіряє успішність відповіді,
 * парсить тіло відповіді як JSON і повертає розпарсені дані.
 *
 * Викидає помилку, якщо запит не був успішним (наприклад, файл не знайдено або помилка сервера).
 */
export async function loadJson(path) {
  // Виконання HTTP GET-запиту до вказаного шляху
  const response = await fetch(path);

  // Перевірка статусу відповіді: якщо не 2xx, викидається помилка із статусом
  if (!response.ok)
    throw new Error(`Failed to load ${path}: ${response.statusText}`);

  // Парсинг тіла відповіді як JSON
  const data = await response.json();

  // Повернення розпарсених даних
  return data;
}
